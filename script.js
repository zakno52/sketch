function generateScale() {
  // reset canvas every time you choose different scale
  canvas.innerHTML = "";

  // the canvase is square so scaleNumber*scaleNumber
  for (let index = 0; index < scaleNumber.value * scaleNumber.value; index++) {
    canvas.appendChild(creatDivs.cloneNode(true));
    divss[index].style.opacity = "1";
  }

  // adding grid properties
  canvas.style.gridTemplateColumns = `repeat(${scaleNumber.value}, 1fr)`;
  canvas.style.gridTemplateRows = `repeat(${scaleNumber.value}, 1fr)`;

  // Track if the mouse is being clicked or not
  document.addEventListener("mousedown", () => {
    isMouseDown = true;
  });
  document.addEventListener("mouseup", () => {
    isMouseDown = false;
  });

  // prevent dragging
  divss.forEach((e) => {
    e.addEventListener("dragstart", (event) => {
      event.preventDefault();
    });

    // give every childNone an eventlistner so we can change color on hover
    e.addEventListener("mouseover", () => {
      if (isMouseDown) {
        if (erase.checked === true) {
          e.style.backgroundColor = "white";
        } else if (colors.checked === true && erase.checked !== true) {
          e.style.backgroundColor = `
          rgb(
          ${Math.random() * 255},
          ${Math.random() * 255},
          ${Math.random() * 255}
          )`;
          // darken every square by 10%
          let currentOpacity = parseFloat(e.style.opacity);
          e.style.opacity = `${currentOpacity - 0.1}`;
        } else {
          e.style.backgroundColor = "black";
        }
      }
    });
  });
}

